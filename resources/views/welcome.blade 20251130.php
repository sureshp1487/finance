        // Form Validation and AJAX Submission
        $(document).ready(function() {
            $('#contactForm').on('submit', function(e) {
                e.preventDefault();
                
                // Reset validation states
                $('.is-invalid').removeClass('is-invalid');
                $('.invalid-feedback').text('');
                
                // Get form data
                const formData = {
                    name: $('#name').val().trim(),
                    email: $('#email').val().trim(),
                    phone: $('#phone').val().trim(),
                    loan_amount: $('#loan_amount').val().trim(),
                    loan_type: $('#loan_type').val(),
                    city: $('#city').val().trim(),
                    message: $('#message').val().trim(),
                    _token: $('input[name="_token"]').val()
                };
                
                // Validate form
                let isValid = true;
                
                // Name validation
                if (!formData.name) {
                    $('#name').addClass('is-invalid');
                    $('#nameError').text('Please enter your full name');
                    isValid = false;
                }
                
                // Email validation
                const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
                if (!formData.email) {
                    $('#email').addClass('is-invalid');
                    $('#emailError').text('Please enter your email address');
                    isValid = false;
                } else if (!emailRegex.test(formData.email)) {
                    $('#email').addClass('is-invalid');
                    $('#emailError').text('Please enter a valid email address');
                    isValid = false;
                }
                
                // Phone validation (Indian mobile numbers)
                const phoneRegex = /^[6-9]\d{9}$/;
                if (!formData.phone) {
                    $('#phone').addClass('is-invalid');
                    $('#phoneError').text('Please enter your phone number');
                    isValid = false;
                } else if (!phoneRegex.test(formData.phone.replace(/\D/g, ''))) {
                    $('#phone').addClass('is-invalid');
                    $('#phoneError').text('Please enter a valid 10-digit Indian mobile number');
                    isValid = false;
                }
                
                // Loan amount validation
                if (!formData.loan_amount) {
                    $('#loan_amount').addClass('is-invalid');
                    $('#loanAmountError').text('Please enter the loan amount');
                    isValid = false;
                } else if (formData.loan_amount < 50000 || formData.loan_amount > 5000000) {
                    $('#loan_amount').addClass('is-invalid');
                    $('#loanAmountError').text('Loan amount must be between ₹50,000 and ₹50,00,000');
                    isValid = false;
                }
                
                // Loan type validation
                if (!formData.loan_type) {
                    $('#loan_type').addClass('is-invalid');
                    $('#loanTypeError').text('Please select a loan type');
                    isValid = false;
                }
                
                // City validation
                if (!formData.city) {
                    $('#city').addClass('is-invalid');
                    $('#cityError').text('Please enter your location in Chennai');
                    isValid = false;
                }
                
                if (!isValid) {
                    toastr.error('Please correct the errors in the form');
                    return;
                }
                
                // Show loading state
                $('#submitBtn').prop('disabled', true);
                $('#submitText').text('Submitting...');
                $('#submitSpinner').removeClass('d-none');
                
                // AJAX submission
                $.ajax({
                    url: '/admin/contact/submit', // Update this URL to match your Laravel route
                    method: 'POST',
                    data: formData,
                    success: function(response) {
                        // Reset form
                        $('#contactForm')[0].reset();
                        
                        // Show success message
                        toastr.success('Your loan application has been submitted successfully! We will contact you within 2 hours.');
                        
                        // Reset button state
                        $('#submitBtn').prop('disabled', false);
                        $('#submitText').text('Submit Loan Application');
                        $('#submitSpinner').addClass('d-none');
                    },
                    error: function(xhr) {
                        let errorMessage = 'An error occurred while submitting your application. Please try again.';
                        
                        if (xhr.responseJSON && xhr.responseJSON.errors) {
                            const errors = xhr.responseJSON.errors;
                            
                            // Display validation errors
                            for (const field in errors) {
                                const errorElement = $(`#${field}Error`);
                                const inputElement = $(`#${field}`);
                                
                                if (errorElement.length && inputElement.length) {
                                    inputElement.addClass('is-invalid');
                                    errorElement.text(errors[field][0]);
                                }
                            }
                            
                            errorMessage = 'Please correct the errors in the form';
                        }
                        
                        toastr.error(errorMessage);
                        
                        // Reset button state
                        $('#submitBtn').prop('disabled', false);
                        $('#submitText').text('Submit Loan Application');
                        $('#submitSpinner').addClass('d-none');
                    }
                });
            });
            
            // Real-time validation
            $('#name, #email, #phone, #loan_amount, #loan_type, #city').on('input', function() {
                $(this).removeClass('is-invalid');
                $(`#${this.id}Error`).text('');
            });
        });
